/**
 * Normalize a MAC address to bare uppercase hex (12 chars).
 * Accepts colon, hyphen, Cisco dot, or bare formats.
 * Returns null if invalid.
 */
export function normalizeMac(input) {
  if (!input || typeof input !== 'string') return null;
  const stripped = input.replace(/[\s.:\-]/g, '').toUpperCase();
  if (!/^[0-9A-F]{12}$/.test(stripped)) return null;
  return stripped;
}

/**
 * Validate a MAC address string (any format).
 */
export function isValidMac(input) {
  return normalizeMac(input) !== null;
}

/**
 * Format a normalized MAC (12 hex chars) to various formats.
 */
export function formatMac(bare) {
  if (!bare || bare.length !== 12) return {};
  const upper = bare.toUpperCase();
  const lower = bare.toLowerCase();
  return {
    colon: upper.match(/.{2}/g).join(':'),
    hyphen: upper.match(/.{2}/g).join('-'),
    cisco: lower.match(/.{4}/g).join('.'),
    bare: upper,
  };
}

/**
 * Look up the vendor for a MAC address using the local OUI database.
 * Returns the vendor string or null if not found.
 */
export function lookupVendorLocal(bare) {
  if (!bare || bare.length < 6) return null;
  const prefix = bare.substring(0, 6).toUpperCase();
  return OUI_DB[prefix] || null;
}

/**
 * Look up the vendor for a MAC using the maclookup.app API (fallback).
 * Returns a promise that resolves to { vendor, found }.
 */
export async function lookupVendorOnline(bare) {
  if (!bare || bare.length < 6) return { vendor: null, found: false };
  const mac = bare.substring(0, 6).toUpperCase();
  const formatted = mac.match(/.{2}/g).join(':');
  try {
    const res = await fetch(`https://api.maclookup.app/v2/macs/${formatted}`, {
      signal: AbortSignal.timeout(5000),
    });
    if (!res.ok) return { vendor: null, found: false };
    const data = await res.json();
    if (data.found && data.company) {
      return { vendor: data.company, found: true };
    }
    return { vendor: null, found: false };
  } catch {
    return { vendor: null, found: false };
  }
}
